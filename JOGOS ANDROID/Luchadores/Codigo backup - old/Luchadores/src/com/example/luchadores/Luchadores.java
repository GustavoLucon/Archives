package com.example.luchadores;

public class Luchadores {
	
	private int Vida;
	private int Especial;
	

	public Luchadores(int vida, int especial) {
		super();
		Vida = vida;
		Especial = especial;
	}
	public int getVida() {
		return Vida;
	}
	public void setVida(int vida) {
		Vida = vida;
	}
	public int getEspecial() {
		return Especial;
	}
	public void setEspecial(int especial) {
		Especial = especial;
	}
	
	
	
	
	

}
