	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		// Codigo do boatao pingar
		
		//setup do botao(colocar tamanho ,fonte , mensagem e bloquear os botoes)
		
		Font font = new Font("Verdana", Font.PLAIN, 16);
		TelaInicial.jTextArea1.setFont(font);
		TelaInicial.jTextArea1.setText("Processando...");
		TelaInicial.jButton1.setEnabled(false);
		TelaInicial.jButton2.setEnabled(false);
		
		//fim do setup
		
		//Procedimento para pingar gerando thread caso o botao de repetir esteja desligado
		
		if (TelaInicial.jCheckBox2.isSelected() == false) {
			java.awt.EventQueue.invokeLater(new Runnable() {
				public void run() {
					Pingar ping = new Pingar(TelaInicial.jCheckBox1
							.isSelected());
					TelaInicial.jTextArea1.setText(ping.getTexto());
					TelaInicial.jButton1.setEnabled(true);
					TelaInicial.jButton2.setEnabled(true);
					if (ping.getErroDePing()) {
						TelaInicial.jTextArea2.setText(TelaInicial.jTextArea2
								.getText()
								+ "Houve um erro as "
								+ Horas()
								+ "\n");
						if (ping.getfazerBarulho()) {
							Toolkit.getDefaultToolkit().beep();
						}
					}
				}
			});
		} else {
			//Procedimento para pingar gerando thread caso o botao de repetir esteja ligado
			TelaInicial.jTextArea1.setText("");
			int repetir = Integer.parseInt(JOptionPane
					.showInputDialog("Repetir quantas vezes?"));
			for (int i = 0; i < repetir; i++) {
				PingRepetido ping = new PingRepetido();
				ping.rodar();			
			}
			TelaInicial.jButton1.setEnabled(true);
			TelaInicial.jButton2.setEnabled(true);
		}

	}

	private static String Horas() {
		//Obter o Horario
		String data = "dd/MM/yyyy";
		String hora = "h:mm - a";
		String hora1;

		java.util.Date agora = new java.util.Date();
		;
		SimpleDateFormat formata = new SimpleDateFormat(data);
		formata = new SimpleDateFormat(hora);
		hora1 = formata.format(agora);
		return hora1;

	}

	private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
		// Criar o arquivo
		TelaInicial.jButton1.setEnabled(false);
		TelaInicial.jButton2.setEnabled(false);
		TelaInicial.jTextArea1.setText("Gerando...");
		java.awt.EventQueue.invokeLater(new Runnable() {
			public void run() {
				Pingar ping = new Pingar(TelaInicial.jCheckBox1.isSelected());
				GerarTxt gerador = new GerarTxt();
				gerador.GerarArquivoTxt(ping.getTexto());
				TelaInicial.jButton1.setEnabled(true);
				TelaInicial.jButton2.setEnabled(true);
				TelaInicial.jTextArea1.setText("Gerado Relatorio");
				if (ping.getErroDePing()) {
					Toolkit.getDefaultToolkit().beep();
				}
			}
		});
	}

