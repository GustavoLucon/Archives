	public class PingRepetido {
		//Ping sendo repetido
		//Onde occorre o erro <==================
		public void rodar() {
			Pingar ping = new Pingar(TelaInicial.jCheckBox1.isSelected());	
			TelaInicial.jTextArea1.setText(jTextArea1.getText() + "\n"
					+ ping.getTexto());
			if (ping.getErroDePing()) {
				TelaInicial.jTextArea2.setText(TelaInicial.jTextArea2.getText()
						+ "Houve um erro as " + Horas() + "\n");
				if (ping.getfazerBarulho()) {
					Toolkit.getDefaultToolkit().beep();
				}
			}
		}
	}
